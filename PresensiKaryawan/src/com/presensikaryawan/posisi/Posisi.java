package com.presensikaryawan.posisi;

/**
 *
 * @author Tinus
 */
public class Posisi {
    private String kode_posisi;
    private String nama_posisi;

    public Posisi() {
    }

    /**
     * @return the kode_posisi
     */
    public String getKode_posisi() {
        return kode_posisi;
    }

    /**
     * @param kode_posisi the kode_posisi to set
     */
    public void setKode_posisi(String kode_posisi) {
        this.kode_posisi = kode_posisi;
    }

    /**
     * @return the nama_posisi
     */
    public String getNama_posisi() {
        return nama_posisi;
    }

    /**
     * @param nama_posisi the nama_posisi to set
     */
    public void setNama_posisi(String nama_posisi) {
        this.nama_posisi = nama_posisi;
    }
    
    
}
