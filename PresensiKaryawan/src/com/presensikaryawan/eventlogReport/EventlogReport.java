package com.presensikaryawan.eventlogReport;

/**
 *
 * @author Tinus
 */
public class EventlogReport {
    private int no;
    private String dateLog, timeLog, nip, nama, fkMode, verify, deviceID, numCard;

    public EventlogReport() {
    }

    /**
     * @return the no
     */
    public int getNo() {
        return no;
    }

    /**
     * @param no the no to set
     */
    public void setNo(int no) {
        this.no = no;
    }

    /**
     * @return the dateLog
     */
    public String getDateLog() {
        return dateLog;
    }

    /**
     * @param dateLog the dateLog to set
     */
    public void setDateLog(String dateLog) {
        this.dateLog = dateLog;
    }

    /**
     * @return the timeLog
     */
    public String getTimeLog() {
        return timeLog;
    }

    /**
     * @param timeLog the timeLog to set
     */
    public void setTimeLog(String timeLog) {
        this.timeLog = timeLog;
    }

    /**
     * @return the nip
     */
    public String getNip() {
        return nip;
    }

    /**
     * @param nip the nip to set
     */
    public void setNip(String nip) {
        this.nip = nip;
    }

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }

    /**
     * @return the fkMode
     */
    public String getFkMode() {
        return fkMode;
    }

    /**
     * @param fkMode the fkMode to set
     */
    public void setFkMode(String fkMode) {
        this.fkMode = fkMode;
    }

    /**
     * @return the verify
     */
    public String getVerify() {
        return verify;
    }

    /**
     * @param verify the verify to set
     */
    public void setVerify(String verify) {
        this.verify = verify;
    }

    /**
     * @return the deviceID
     */
    public String getDeviceID() {
        return deviceID;
    }

    /**
     * @param deviceID the deviceID to set
     */
    public void setDeviceID(String deviceID) {
        this.deviceID = deviceID;
    }

    /**
     * @return the numCard
     */
    public String getNumCard() {
        return numCard;
    }

    /**
     * @param numCard the numCard to set
     */
    public void setNumCard(String numCard) {
        this.numCard = numCard;
    }
    
    
}
