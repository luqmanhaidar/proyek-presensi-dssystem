package com.presensikaryawan.payrollBankReport;

/**
 *
 * @author Tinus
 */
public class PayrollBankReport {
    private String noRekening;
    private String nama;
    private String bulanTahun;
    private double jumlahGaji;
    private double totalKeseluruhanGaji;
    private int no;

    public PayrollBankReport() {
    }

    /**
     * @return the noRekening
     */
    public String getNoRekening() {
        return noRekening;
    }

    /**
     * @param noRekening the noRekening to set
     */
    public void setNoRekening(String noRekening) {
        this.noRekening = noRekening;
    }

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }

    /**
     * @return the bulanTahun
     */
    public String getBulanTahun() {
        return bulanTahun;
    }

    /**
     * @param bulanTahun the bulanTahun to set
     */
    public void setBulanTahun(String bulanTahun) {
        this.bulanTahun = bulanTahun;
    }

    /**
     * @return the jumlahGaji
     */
    public double getJumlahGaji() {
        return jumlahGaji;
    }

    /**
     * @param jumlahGaji the jumlahGaji to set
     */
    public void setJumlahGaji(double jumlahGaji) {
        this.jumlahGaji = jumlahGaji;
    }

    /**
     * @return the totalKeseluruhanGaji
     */
    public double getTotalKeseluruhanGaji() {
        return totalKeseluruhanGaji;
    }

    /**
     * @param totalKeseluruhanGaji the totalKeseluruhanGaji to set
     */
    public void setTotalKeseluruhanGaji(double totalKeseluruhanGaji) {
        this.totalKeseluruhanGaji = totalKeseluruhanGaji;
    }

    /**
     * @return the no
     */
    public int getNo() {
        return no;
    }

    /**
     * @param no the no to set
     */
    public void setNo(int no) {
        this.no = no;
    }
    
    
    
}
