package com.presensikaryawan.jamMasukKeluarReport;

/**
 *
 * @author Tinus
 */
public class JamMasukKeluarReport {
    private String nip, nama, jamMasuk, jamKeluar, tanggal;

    public JamMasukKeluarReport() {
    }

    /**
     * @return the nip
     */
    public String getNip() {
        return nip;
    }

    /**
     * @param nip the nip to set
     */
    public void setNip(String nip) {
        this.nip = nip;
    }

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }

    /**
     * @return the jamMasuk
     */
    public String getJamMasuk() {
        return jamMasuk;
    }

    /**
     * @param jamMasuk the jamMasuk to set
     */
    public void setJamMasuk(String jamMasuk) {
        this.jamMasuk = jamMasuk;
    }

    /**
     * @return the jamKeluar
     */
    public String getJamKeluar() {
        return jamKeluar;
    }

    /**
     * @param jamKeluar the jamKeluar to set
     */
    public void setJamKeluar(String jamKeluar) {
        this.jamKeluar = jamKeluar;
    }

    /**
     * @return the tanggal
     */
    public String getTanggal() {
        return tanggal;
    }

    /**
     * @param tanggal the tanggal to set
     */
    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }
}
