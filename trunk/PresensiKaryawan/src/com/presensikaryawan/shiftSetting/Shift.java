package com.presensikaryawan.shiftSetting;

/**
 *
 * @author Tinus
 */
public class Shift {
    private String kodeShift;
    private String namaShift;
    private String waktuMulai;
    private String waktuSelesai;

    public Shift() {
    }

    /**
     * @return the kodeShift
     */
    public String getKodeShift() {
        return kodeShift;
    }

    /**
     * @param kodeShift the kodeShift to set
     */
    public void setKodeShift(String kodeShift) {
        this.kodeShift = kodeShift;
    }

    /**
     * @return the namaShift
     */
    public String getNamaShift() {
        return namaShift;
    }

    /**
     * @param namaShift the namaShift to set
     */
    public void setNamaShift(String namaShift) {
        this.namaShift = namaShift;
    }

    /**
     * @return the waktuMulai
     */
    public String getWaktuMulai() {
        return waktuMulai;
    }

    /**
     * @param waktuMulai the waktuMulai to set
     */
    public void setWaktuMulai(String waktuMulai) {
        this.waktuMulai = waktuMulai;
    }

    /**
     * @return the waktuSelesai
     */
    public String getWaktuSelesai() {
        return waktuSelesai;
    }

    /**
     * @param waktuSelesai the waktuSelesai to set
     */
    public void setWaktuSelesai(String waktuSelesai) {
        this.waktuSelesai = waktuSelesai;
    }
    
    
}
