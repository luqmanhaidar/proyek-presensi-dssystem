package com.presensikaryawan.shiftReport;

/**
 *
 * @author Tinus
 */
public class ShiftReport {
    private String nip, tanggal, nama, kode_shift;

    public ShiftReport() {
    }

    /**
     * @return the nip
     */
    public String getNip() {
        return nip;
    }

    /**
     * @param nip the nip to set
     */
    public void setNip(String nip) {
        this.nip = nip;
    }

    /**
     * @return the tanggal
     */
    public String getTanggal() {
        return tanggal;
    }

    /**
     * @param tanggal the tanggal to set
     */
    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }

    /**
     * @return the kode_shift
     */
    public String getKode_shift() {
        return kode_shift;
    }

    /**
     * @param kode_shift the kode_shift to set
     */
    public void setKode_shift(String kode_shift) {
        this.kode_shift = kode_shift;
    }
    
}
