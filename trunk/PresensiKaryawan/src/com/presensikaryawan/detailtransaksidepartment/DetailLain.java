package com.presensikaryawan.detailtransaksidepartment;

/**
 *
 * @author Tinus
 */
public class DetailLain {
    private String nip, tanggal;
    private double potonganLain;
    private double prestasi;

    public DetailLain() {
    }

    /**
     * @return the nip
     */
    public String getNip() {
        return nip;
    }

    /**
     * @param nip the nip to set
     */
    public void setNip(String nip) {
        this.nip = nip;
    }

    /**
     * @return the tanggal
     */
    public String getTanggal() {
        return tanggal;
    }

    /**
     * @param tanggal the tanggal to set
     */
    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    /**
     * @return the potonganLain
     */
    public double getPotonganLain() {
        return potonganLain;
    }

    /**
     * @param potonganLain the potonganLain to set
     */
    public void setPotonganLain(double potonganLain) {
        this.potonganLain = potonganLain;
    }

    /**
     * @return the prestasi
     */
    public double getPrestasi() {
        return prestasi;
    }

    /**
     * @param prestasi the prestasi to set
     */
    public void setPrestasi(double prestasi) {
        this.prestasi = prestasi;
    }


    
    
}
