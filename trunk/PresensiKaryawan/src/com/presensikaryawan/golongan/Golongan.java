package com.presensikaryawan.golongan;

/**
 *
 * @author Eki
 */
public class Golongan {
    
    private String kodeGolongan;
    private String namaGolongan;
    private double gajiPokok;
    private double uangMakan;
    private double uangLembur;
    private double uangHadir;
    private double potonganTelat;

    /**
     * @return the kodeGolongan
     */
    public String getKodeGolongan() {
        return kodeGolongan;
    }

    /**
     * @param kodeGolongan the kodeGolongan to set
     */
    public void setKodeGolongan(String kodeGolongan) {
        this.kodeGolongan = kodeGolongan;
    }

    /**
     * @return the namaGolongan
     */
    public String getNamaGolongan() {
        return namaGolongan;
    }

    /**
     * @param namaGolongan the namaGolongan to set
     */
    public void setNamaGolongan(String namaGolongan) {
        this.namaGolongan = namaGolongan;
    }
    
    /**
     * @return the gajiPokok
     */
    public double getGajiPokok() {
        return gajiPokok;
    }

    /**
     * @param gajiPokok the gajiPokok to set
     */
    public void setGajiPokok(double gajiPokok) {
        this.gajiPokok = gajiPokok;
    }

    /**
     * @return the uangMakan
     */
    public double getUangMakan() {
        return uangMakan;
    }

    /**
     * @param uangMakan the uangMakan to set
     */
    public void setUangMakan(double uangMakan) {
        this.uangMakan = uangMakan;
    }

    /**
     * @return the uangLembur
     */
    public double getUangLembur() {
        return uangLembur;
    }

    /**
     * @param uangLembur the uangLembur to set
     */
    public void setUangLembur(double uangLembur) {
        this.uangLembur = uangLembur;
    }

    /**
     * @return the uangHadir
     */
    public double getUangHadir() {
        return uangHadir;
    }

    /**
     * @param uangHadir the uangHadir to set
     */
    public void setUangHadir(double uangHadir) {
        this.uangHadir = uangHadir;
    }

    /**
     * @return the potonganTelat
     */
    public double getPotonganTelat() {
        return potonganTelat;
    }

    /**
     * @param potonganTelat the potonganTelat to set
     */
    public void setPotonganTelat(double potonganTelat) {
        this.potonganTelat = potonganTelat;
    }
    
}
